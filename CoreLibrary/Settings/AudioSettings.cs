/// <summary>
/// The audio settings of the game.
/// </summary>
public class AudioSettings
{
    /// <summary>
    /// The music volume of the game.
    /// Default is 50%.
    /// </summary>
    public float MusicVolume { get; set; } = 0.5f;

    /// <summary>
    /// The sfx volume of the game.
    /// Default is 50%.
    /// </summary>
    public float SfxVolume { get; set; } = 0.5f;
}